package io.younghwang.javadesignpattern.command;

public class GarbageDoor {

    public void up() {
        System.out.println("Garbage Door Up");
    }

    public void down() {
        System.out.println("Garbage Door Down");
    }

    public void stop() {
        System.out.println("Garbage Door Stop");
    }

    public void lightOn() {
        System.out.println("Garbage Door Light On");
    }

    public void lightOff() {
        System.out.println("Garbage Door Light Off");
    }

}
